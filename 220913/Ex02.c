//practice

#include <stdio.h>

int main() {
	// 올해의 연도
	printf("%d\n", 2022);

	// 내 키
	printf("%lfcm\n", 158.9);

	// 대문자 Z
	printf("%c\n", 'C');

	// 사과 10개는 9000원입니다 
	printf("사과 %d개는 %d원입니다\n", 10, 9000);

	// 나의 성적은 4.3점입니다
	printf("나의 성적은 %lf점입니다\n", 4.3);

	// A는 B보다 앞에 있습니다
	printf("%c는 %c보다 앞에 있습니다\n", 'A', 'B');

	return 0;
}